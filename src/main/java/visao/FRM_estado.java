public void Preenchertabela(String SQL){
    ArrayList dados = new ArrayList();
    String[] Colunas = new String[]{"ID","NOME","SIGLA"};
    conecta.executaSQL(SQL);
    
        try {
            conecta.rs.first();
            do{
                dados.add(new Object[]{conecta.rs.getInt("id_estado"),conecta.rs.getString("nome_estado"), conecta.rs.getString("sigla_estado")});
            
            }while(conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "erro");
        }
        ModeloTabela modelo = new ModeloTabela (dados , Colunas);
        tabela_tabela.setModel(modelo);
        tabela_tabela.getColumnModel().getColumn(0).setPreferredWidth(250);
        tabela_tabela.getColumnModel().getColumn(0).setResizable(false);
        tabela_tabela.getColumnModel().getColumn(1).setPreferredWidth(380);
        tabela_tabela.getColumnModel().getColumn(1).setResizable(false);
        tabela_tabela.getColumnModel().getColumn(2).setPreferredWidth(120);
        tabela_tabela.getColumnModel().getColumn(2).setResizable(false);
        tabela_tabela.getTableHeader().setReorderingAllowed(false);
        tabela_tabela.setAutoResizeMode(tabela_tabela.AUTO_RESIZE_OFF);
        tabela_tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        
        
}      
