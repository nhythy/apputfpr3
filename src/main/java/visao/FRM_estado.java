    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
      
        try {
            PreparedStatement pst = conecta.conn.prepareCall("insert into estados(nome_estado, sigla_estado)values (?,?)");
            
            
            
            
            
             pst.setString(1,jtf_nome.getText());
             pst.setString(2,jtf_sigla.getText());
             pst.executeUpdate();
            
            JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!");
            
           
            
            
           
             
           
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "ERRO NA INSERÇAO!");
        }
        
        Preenchertabela("select * from estados order by id_estado ");
        jtf_nome.setText("");
       jtf_codigo.setText("");
       jtf_sigla.setText("");
        
        
        
        
    }//GEN-LAST:event_btn_salvarActionPerformed

